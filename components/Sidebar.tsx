'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { signOut } from 'next-auth/react'
import { cn } from '@/lib/utils'

const adminNav = [
  { href: '/dashboard',  label: 'Dashboard',    icon: HomeIcon },
  { href: '/clients',    label: 'Clients',       icon: UsersIcon },
  { href: '/briefs',     label: 'Briefs',        icon: DocumentIcon },
  { href: '/generate',   label: 'Generate',      icon: SparklesIcon },
  { href: '/calendar',   label: 'Calendar',      icon: CalendarIcon },
  { href: '/approvals',  label: 'Approvals',     icon: CheckCircleIcon },
  { href: '/export',     label: 'Export',        icon: DownloadIcon },
]

const adminOnlyNav = [
  { href: '/team', label: 'Team', icon: TeamIcon },
]

interface Props {
  role: string
  userName: string
}

export default function Sidebar({ role, userName }: Props) {
  const pathname = usePathname()

  const navItems = role === 'ADMIN' ? [...adminNav, ...adminOnlyNav] : adminNav

  return (
    <aside className="w-64 bg-white border-r border-gray-200 flex flex-col shrink-0">
      {/* Brand */}
      <div className="px-6 py-5 border-b border-gray-200">
        <div className="flex items-center gap-3">
          <div className="w-8 h-8 rounded-lg bg-blue-600 flex items-center justify-center shrink-0">
            <svg className="w-5 h-5 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
                d="M7 4v16M17 4v16M3 8h4m10 0h4M3 12h18M3 16h4m10 0h4" />
            </svg>
          </div>
          <span className="font-semibold text-gray-900 text-sm leading-tight">
            {process.env.NEXT_PUBLIC_APP_NAME ?? 'Social Media Tool'}
          </span>
        </div>
      </div>

      {/* Nav */}
      <nav className="flex-1 px-3 py-4 space-y-0.5 overflow-y-auto">
        {navItems.map(({ href, label, icon: Icon }) => {
          const active = pathname === href || pathname.startsWith(href + '/')
          return (
            <Link
              key={href}
              href={href}
              className={cn(
                'flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors',
                active
                  ? 'bg-blue-50 text-blue-700'
                  : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
              )}
            >
              <Icon className={cn('w-4 h-4 shrink-0', active ? 'text-blue-600' : 'text-gray-400')} />
              {label}
            </Link>
          )
        })}
      </nav>

      {/* User + Sign out */}
      <div className="px-3 py-4 border-t border-gray-200">
        <div className="flex items-center gap-3 px-3 py-2 mb-1">
          <div className="w-7 h-7 rounded-full bg-blue-100 flex items-center justify-center shrink-0">
            <span className="text-xs font-semibold text-blue-700">
              {userName.charAt(0).toUpperCase()}
            </span>
          </div>
          <div className="min-w-0">
            <p className="text-sm font-medium text-gray-900 truncate">{userName}</p>
            <p className="text-xs text-gray-400">{role === 'ADMIN' ? 'Administrator' : 'Team Member'}</p>
          </div>
        </div>
        <Link
          href="/settings"
          className="w-full flex items-center gap-3 px-3 py-2 text-sm text-gray-500 hover:text-gray-700 hover:bg-gray-50 rounded-lg transition-colors"
        >
          <SettingsIcon className="w-4 h-4 shrink-0 text-gray-400" />
          Change Password
        </Link>
        <button
          onClick={() => signOut({ callbackUrl: '/login' })}
          className="w-full flex items-center gap-3 px-3 py-2 text-sm text-gray-500 hover:text-gray-700 hover:bg-gray-50 rounded-lg transition-colors"
        >
          <LogOutIcon className="w-4 h-4 shrink-0 text-gray-400" />
          Sign out
        </button>
      </div>
    </aside>
  )
}

